/*2) Escreva um programa em Java para imprimir os números de 1 (inclusive) a 10 (inclusive) em ordem
decrescente. */

// Gabriel Apolinário Fabrício
public class ex02 {
    public static void main(String[] args) {
        int cont = 10;
        while ( cont >= 1) {
            System.out.println("Contagem: " + cont);
            cont--;
        }
    }
}
